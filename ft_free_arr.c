#include "libft.h"

char		**ft_free_arr(char **arr)
{
	size_t		i;

	i = 0;
	while (*arr[i])
	{
		free(arr[i]);
		arr[i] = NULL;
		i++;
	}
	free(arr);
	return (NULL);
}
